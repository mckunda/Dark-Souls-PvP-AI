pub fn StaminaDrainForAttack(weapon_id: u32, animationType_id: u16) -> i32 {
    //only interested in weapon type, above 100's place
    let weaponType: u32 = weapon_id / 1000;
    //first match weapon id to behavior variation id
    let BehaviorVariationId: u16 = WeaponTypeToBehaviorVariation(weaponType);
    //concatinate behavior and animation type to allow hash table lookup
    let Behavior_AnimationType_hash: u16 = (BehaviorVariationId * 1000) + animationType_id;
    //then match behavior veriation id and animation type to stamina drain
    BehaviorAndAttackTypeToStaminaDrain(Behavior_AnimationType_hash as u32)
}

