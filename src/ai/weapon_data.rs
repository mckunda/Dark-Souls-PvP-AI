//convert to same id for same hash handling
pub fn CompressAnimationTypes(animationType_id: u16) -> u16 {
    match animationType_id {
        //1h rolling attack
        41 => 46,
        //1h r1 swing 2
        48 => 46,
        //1h r1 swing 3
        49 => 46,
        //1h r1 into r2 combo
        88 => 89,
        //1h r2 combo
        90 => 89,
        //rolling attack 2h
        103 => 107,
        //2h r1 combo 1
        109 => 107,
        //2h r1 combo 2
        110 => 107,
        //2h r1 into r2 combo
        114 => 115,
        //2h r2 combo
        116 => 115,
        _ => animationType_id,
    }
}

pub fn PoiseDamageForAttack(weapon_id: u32, animationType_id: u16) -> f32 {
    //only interested in weapon type, above 100's place
    let weaponType: u32 = weapon_id / 1000;
    //concatinate weaponType and animation type to allow hash table lookup
    let Weapon_AnimationType_hash: u32 = (weaponType * 1000) + CompressAnimationTypes(animationType_id) as u32;
    //then match weapon type id and animation type to poise damage
    WeaponAndAttackTypeToPoiseDamage(Weapon_AnimationType_hash)
}

